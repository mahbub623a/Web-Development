// A variable is simply the name of the variable location

// let -> syntax for declaring variable
let a = 5;
let b = 5;
let c;
console.log(c); // undefine

c = a + b;
console.log(c); // 10

let price = 99.9;
console.log(price);

let name = "Mahbub";
console.log(name);

// const -> constant variable, we can not change later
const cgpa = 3.9;
console.log(cgpa);
// cgpa = cgpa + 1; // we can't do it because of constant variable
console.log(cgpa);

// var -> old syntax of writing javascript
var taj = "Tamanna";
console.log(taj);



